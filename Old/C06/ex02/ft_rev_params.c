#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char **argv)
{
	int	i;

	i = argc - 1;
	while (argv[i])
	{
		if (i > 0)
		{
			ft_putstr(argv[i]);
			write(1, "\n", 1);
		}
		i--;
	}
	return (0);
}
