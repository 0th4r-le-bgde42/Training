#include <unistd.h>

void	ft_is_negative(int n)
{
	int		digits;

	if (n < 0)
	{
		digits = 'N';
		write (1, &digits, 1);
	}
	else
	{
		digits = 'P';
		write (1, &digits, 1);
	}
}
