#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	c = 0;
	c = *b;
	*b = *a;
	*a = c;
}

int main()
{
	int a = 1;
	int b = 2;
	printf("a = %d, b = %d\n", a, b);
	ft_swap(&a, &b);
	printf("na = %d, nb = %d\n", a, b);
}
