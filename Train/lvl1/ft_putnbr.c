#include <unistd.h>

void	ft_putnbr(long i)
{
	if (i < 0)
	{
		write(1, "-", 1);
		i = -i;
	}
	if (i > 9)
		ft_putnbr(i / 10);
	write(1, &"0123456789"[i % 10], 1);
}

int main()
{
	int i = -2147483648;
	ft_putnbr(i);
	return (0);
}

