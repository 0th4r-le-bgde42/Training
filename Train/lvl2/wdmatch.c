#include <unistd.h>

int main(int ac, char **av)
{
	int i = -1;
	int j = 0;
	if (ac == 3)
	{
		while (av[2][++i])
			if (av[2][i] == av[1][j])
				j++;
		if (av[1][j] == '\0')
			write(1, &av[1][0], j);
	}
	write(1, "\n", 1);
	return (0);
}
