#include <unistd.h>

void ft_putnbr(long nb)
{
	if (nb > 9)
		ft_putnbr(nb / 10);
	write(1, &"0123456789"[nb % 10], 1);
}

int main(int ac, char **av)
{
	(void) av;
	if (ac > 1)
	{
		ft_putnbr(ac - 1);	
	}
	else
		write(1, "0", 1);
	return (0);
}
